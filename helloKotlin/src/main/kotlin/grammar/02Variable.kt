package grammar

fun main() {

    /**
     * Integer
     */
    println(123)

    // even if it's bigger than Integer.MAX_VALUE, does not matter.
    println(10_000_000_000L)
    println(10_000_000_000_000)

    /**
     * Floating point number
     */
    println(12.34)
    println(56.78f)

    /**
     * Character
     */
    println('a')
    println('A')

    /**
     * String
     */
    print("Hello World")

    /**
     * boolean
     */
    println(true)
    println(false)

}
